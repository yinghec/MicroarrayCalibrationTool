	<h2>How it Works</h2>
		<ol>
  		<li>Each microarray probe hybridized with dilution series of targets</li>
		<li>A relationship between the probe signal intensity and the dilution factor is evaluated</li>
		<li>Several response curves (Freundlich, Linear or Langmuir) are attempted to fit for each probe</li>
		<li>The best fit curve is chosen to be the calibration curve for the probe</li>
		<li>Sample signal intentities in conjunction with the calibration curve are used to calculate target concentration</li>
		</ol>	
